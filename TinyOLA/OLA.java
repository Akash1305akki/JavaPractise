package TinyOLA;

public class OLA {

    private String c_name;
    private String pwd;
    private long c_no;

    public String getC_name() {
        return c_name;
    }

    public void setC_name(String c_name) {
        this.c_name = c_name;
    }

    public String getPwd() {
        return pwd;
    }

    public void setPwd(String pwd) {
        this.pwd = pwd;
    }

    public long getC_no() {
        return c_no;
    }

    public void setC_no(long c_no) {
        this.c_no = c_no;
    }
}